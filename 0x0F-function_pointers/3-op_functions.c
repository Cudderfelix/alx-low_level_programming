/**
 * op_add - adds @a and @b
 *
 * @a: input 1
 * @b: input 2
 *
 * Return: sum of @a and @b
*/

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - gets the difference of @a and @b
 *
 * @a: input 1
 * @b: input 2
 *
 * Return: difference of @a and @b
*/
int op_sub(int a, int b)
{
	return (a - b);
}


